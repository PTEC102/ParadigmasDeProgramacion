    public VisualHorasMundo(String lugar, String hora) {
        initComponents();
        
        this.jLabel1.setText(lugar);
        this.jLabel2.setText(hora);
        
        new Timer(1000, new ActionListener(){
            
            @Override
            public void actionPerformed(ActionEvent e){
                String hora = jLabel2.getText();
                LocalDateTime objHora = LocalDateTime.parse(hora, DateTimeFormatter.ISO_DATE_TIME);
                
                objHora = objHora.plusSeconds(1);
                
                String nuevaHoraFormateada = objHora.format(DateTimeFormatter.ISO_DATE_TIME);
                
                jLabel2.setText(nuevaHoraFormateada);
            }
        }).start();
    }

